#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		x;

	x = 0;
	while (src[x])
		x++;
	dest = (char *)malloc (sizeof(*dest) * x);
	x = 0;
	while (src[x])
	{
		dest[x] = src[x];
		x++;
	}
	dest[x] = '\0';
	return (dest);
}

int ft_iterative_factorial(int nb)
{
	int i;
	int x;

	i = 1;
	x = 1;
	if ((nb < 0) || (nb > 12))
		return (0);
	while (i <= nb)
	{
		x *= i;
		i++;
	}
	return (x);
}

#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	int x;

	x = 0;
	while (s1[x])
		x++;
	while (*s2 && n)
	{
		s1[x] = *s2;
		x++;
		s2++;
		n--;
	}
	s1[x] = '\0';
	return (s1);
}

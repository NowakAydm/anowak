#include "libft.h"

void	ft_tabfree(char **tab)
{
	int x;

	x = 0;
	if (tab)
	{
		while (tab[x])
			free(tab[x++]);
		free(tab[x]);
	}
	free(tab);
	tab = NULL;
}

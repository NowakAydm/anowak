#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	int x;

	x = 0;
	while (s1[x])
		x++;
	while (*s2)
	{
		s1[x] = *s2;
		x++;
		s2++;
	}
	s1[x] = '\0';
	return (s1);
}
